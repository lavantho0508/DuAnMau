void nguoihoc() throws SQLException{
    String query="select manh,HOTEN,GIOITINH,ngaysinh,DIENTHOAI,EMAIL from nguoihoc where\n" +
"manh not in(select nguoihoc.manh from hocvien join KHOAHOC on HOCVIEN.MAKH=KHOAHOC.MAKH\n" +
"join CHUYENDE on CHUYENDE.MACD=KHOAHOC.MACD join NGUOIHOC on NGUOIHOC.MANH=HOCVIEN.MANH)";
    PreparedStatement pre=ConnectSQLServer.Connect.con.prepareStatement(query);
    ResultSet rs=pre.executeQuery();
    while (rs.next()) {    
       list2.add(new NguoiHoc1( rs.getString(1),rs.getString(2),Getgt(rs.getInt(3)),rs.getString(4),rs.getString(5),rs.getString(6)));
        model_nguoihoc.addRow(new Object[]{
        rs.getString(1),rs.getString(2),Getgt(rs.getInt(3)),rs.getString(4),rs.getString(5),rs.getString(6)});
    }
}
