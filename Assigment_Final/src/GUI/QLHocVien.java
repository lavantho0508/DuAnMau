void updateDiem(){
    int index=tbl_hocvien.getSelectedRow();
    String diem=JOptionPane.showInputDialog("Mời bạn nhập điểm", tbl_hocvien.getValueAt(index, 4));
    try {
        if(diem.equals("")){
            JOptionPane.showMessageDialog(this,"Điểm không được bỏ trống");
            return;
        }
        if(diem.matches("[-+]?[0-9]*\\.?[0-9]+$")==false){
            JOptionPane.showMessageDialog(this,"Điểm phải là số");
            return;
        }
        if(Double.valueOf(diem)>10||Double.valueOf(diem)<0){
            JOptionPane.showMessageDialog(this,"Điểm nhập vào không hợp lệ");
            return;
        }
        if(CRUD.CRUD_HocVien.update(Double.valueOf(diem), (int) tbl_hocvien.getValueAt(index, 1))>0){
             addtoTable();
            JOptionPane.showMessageDialog(this, "Sửa điểm thành công");
           
       
        }
        
    } catch (Exception e) {
        e.printStackTrace();
    }
}
