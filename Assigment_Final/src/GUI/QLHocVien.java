"join CHUYENDE on CHUYENDE.MACD=KHOAHOC.MACD join NGUOIHOC on NGUOIHOC.MANH=HOCVIEN.MANH\n" +
"where TENCD=? and chuyende.MACD=?";
     String a = String.valueOf(cbb.getSelectedItem());
