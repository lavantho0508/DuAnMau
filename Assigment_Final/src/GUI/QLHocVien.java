    void loadcbbToTable() {
        String b;
        if (cbb.getSize() == 0) {
            b = "";
        } else {
            String a = String.valueOf(cbb.getSelectedItem());
            b = a.substring(0, a.lastIndexOf("("));
        }

        try {
            String query = "select mahv,nguoihoc.manh,hoten,diem from NGUOIHOC join HOCVIEN\n"
                    + "on NGUOIHOC.MANH=HOCVIEN.MANH join KHOAHOC on HOCVIEN.MAKH=KHOAHOC.MAKH\n"
                    + "where macd=?";
            PreparedStatement pre = ConnectSQLServer.Connect.con.prepareStatement(query);
            pre.setString(1, b);
            ResultSet rs = pre.executeQuery();
            while (rs.next()) {
                list.add(new HocVien(rs.getInt(1), rs.getString(2), rs.getString(3), rs.getDouble(4)));
            }

            loadTable();
        } catch (SQLException ex) {
            Logger.getLogger(QLHocVien.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
