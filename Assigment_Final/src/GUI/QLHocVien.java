int getMKH(String macd) throws SQLException{
    String sql="Select makh from khoahoc where macd=?";
      
    PreparedStatement pre=ConnectSQLServer.Connect.con.prepareStatement(sql);
    pre.setString(1, macd);
    ResultSet rs=pre.executeQuery();
    while (rs.next()) {        
        return rs.getInt(1);
    }
    return -1;
}
